-- OPTIONAL: VIEW ALL DATA JOINED
SELECT 
    SID.STUDENT_ADMISSION_NUMBER,
    SID.STUDENT_ID,
    S.STUDENT_NAME,
    S.GENDER,
    S.CITY,
    S.AGE,
    S.STUDENT_PHONENUMBER,
    C.STUDENT_COURSE,
    C.COURSE_DURATION
FROM STUDENTS_ID SID
JOIN STUDENTS S ON SID.STUDENT_ADMISSION_NUMBER = S.STUDENT_ADMISSION_NUMBER
JOIN COURSES C ON SID.STUDENT_ADMISSION_NUMBER = C.STUDENT_ADMISSION_NUMBER;
